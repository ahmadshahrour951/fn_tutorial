def area(width, height):
    result = width * height
    return result


result = area(5, 6)
print(result)

print(area(10, 15))
print(area(5, 10))


def subtract(num1, num2):
    result = num1 - num2
    return result


print(subtract(15, 10))
print(subtract(0, 5))


def divide(num1, num2):
    result = num1 / num2
    return result


print(divide(18, 6))
print(divide(10, 5))


def add(num1, num2):
    result = num1 + num2
    return result


print(add(15, 10))
print(add(0, 5))
